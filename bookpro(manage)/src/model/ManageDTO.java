package model;

public class ManageDTO {

	private int manage_num;
	private String member_id;
	private String member_grade;
	private int member_mile;
	
	
	public int getManage_num() {
		return manage_num;
	}
	public void setManage_num(int manage_num) {
		this.manage_num = manage_num;
	}
	public String getMember_id() {
		return member_id;
	}
	public void setMember_id(String member_id) {
		this.member_id = member_id;
	}
	public String getMember_grade() {
		return member_grade;
	}
	public void setMember_grade(String member_grade) {
		this.member_grade = member_grade;
	}
	public int getMember_mile() {
		return member_mile;
	}
	public void setMember_mile(int member_mile) {
		this.member_mile = member_mile;
	}
		
}
